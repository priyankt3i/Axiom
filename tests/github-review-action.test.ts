const githubApiRequests: Array<{ method: string; url: string; authorization?: string; body?: string }> = [];
const hermesEvictionRequests: Array<{ method: string; url: string; authorization?: string; body?: string }> = [];
const { privateKey: githubAppPrivateKey } = crypto.generateKeyPairSync("rsa", { modulusLength: 2048 });
const githubAppPrivateKeyPem = githubAppPrivateKey.export({ type: "pkcs8", format: "pem" }).toString();

const githubApiServer = createServer((req, res) => {
  const requestRecord = {
    method: req.method || "",
    url: req.url || "",
    authorization: req.headers.authorization,
    body: undefined as string | undefined,
  };
  githubApiRequests.push(requestRecord);

  if (req.method === "POST" && req.url === "/app/installations/67890/access_tokens") {
    res.setHeader("Content-Type", "application/json");
    res.end(JSON.stringify({
      token: "ghs_do_not_return",
      expires_at: "2030-01-01T00:00:00Z",
      permissions: {
        contents: "write",
        pull_requests: "write",
      },
    }));
    return;
  }

  if (req.method === "GET" && req.url === "/repos/acme/widgets") {
    res.setHeader("Content-Type", "application/json");
    res.end(JSON.stringify({
      full_name: "acme/widgets",
      default_branch: "main",
      private: true,
      permissions: {
        contents: true,
        pull: true,
        push: true,
      },
    }));
    return;
  }

  if (req.method === "GET" && req.url === "/repos/acme/widgets/git/ref/heads/main") {
    res.setHeader("Content-Type", "application/json");
    res.end(JSON.stringify({
      ref: "refs/heads/main",
      object: {
        sha: "abcdef1234567890abcdef1234567890abcdef12",
        type: "commit",
      },
    }));
    return;
  }

  if (req.method === "GET" && req.url === "/repos/acme/widgets/git/ref/heads/feature%2Fhermes-rollback-branch") {
    res.setHeader("Content-Type", "application/json");
    res.end(JSON.stringify({
      ref: "refs/heads/feature/hermes-rollback-branch",
      object: {
        sha: "rollbackbranch123",
        type: "commit",
      },
    }));
    return;
  }

  if (req.method === "DELETE" && req.url === "/repos/acme/widgets/git/refs/heads/feature%2Fhermes-rollback-branch") {
    const chunks: Buffer[] = [];
    req.on("data", (chunk) => chunks.push(Buffer.from(chunk)));
    req.on("end", () => {
      requestRecord.body = Buffer.concat(chunks).toString("utf8");
      res.statusCode = 204;
      res.end();
    });
    return;
  }

  if (req.method === "PUT" && req.url === "/repos/acme/widgets/pulls/42/merge") {
    const chunks: Buffer[] = [];
    req.on("data", (chunk) => chunks.push(Buffer.from(chunk)));
    req.on("end", () => {
      requestRecord.body = Buffer.concat(chunks).toString("utf8");
      res.setHeader("Content-Type", "application/json");
      res.end(JSON.stringify({
        sha: "merge123abc",
        merged: true,
        message: "Pull Request successfully merged",
      }));
    });
    return;
  }

  res.statusCode = 404;
  res.setHeader("Content-Type", "application/json");
  res.end(JSON.stringify({ message: "not found" }));
});

const hermesEvictionServer = createServer((req, res) => {
  const requestRecord = {
    method: req.method || "",
    url: req.url || "",
    authorization: req.headers.authorization,
    body: undefined as string | undefined,
  };
  hermesEvictionRequests.push(requestRecord);

  if (req.method === "POST" && req.url === "/evict") {
    const chunks: Buffer[] = [];
    req.on("data", (chunk) => chunks.push(Buffer.from(chunk)));
    req.on("end", () => {
      requestRecord.body = Buffer.concat(chunks).toString("utf8");
      res.setHeader("Content-Type", "application/json");
      res.end(JSON.stringify({ evictionId: "evict_rollback_123" }));
    });
    return;
  }

  res.statusCode = 404;
  res.setHeader("Content-Type", "application/json");
  res.end(JSON.stringify({ message: "not found" }));
});

