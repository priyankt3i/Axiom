const { startServer } = await import("../server.ts");

let server: Server;
let baseUrl = "";

async function readJson<T>(response: Response): Promise<T> {
  const text = await response.text();
  return JSON.parse(text) as T;
}

function cookieFrom(response: Response) {
  const cookie = response.headers.get("set-cookie")?.split(";")[0];
  assert.ok(cookie, "expected response to include a session cookie");
  return cookie;
}

test.before(async () => {
  server = await startServer({
    port: 0,
    host: "127.0.0.1",
    serveClient: false,
  });
  const address = server.address();
  assert.ok(address && typeof address === "object");
  baseUrl = `http://127.0.0.1:${address.port}`;
});

test.after(async () => {
  if (server) {
    await new Promise<void>((resolve, reject) => {
      server.close((error) => (error ? reject(error) : resolve()));
    });
  }
  await new Promise<void>((resolve, reject) => {
    githubApiServer.close((error) => (error ? reject(error) : resolve()));
  });
  await new Promise<void>((resolve, reject) => {
    hermesEvictionServer.close((error) => (error ? reject(error) : resolve()));
  });
  await fs.rm(dataDir, { recursive: true, force: true });
});

test("production review actions use GitHub App and Hermes adapters", async () => {
  let response = await fetch(`${baseUrl}/api/auth/dev-login`, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      "X-Dev-Login-Token": process.env.DEV_LOGIN_TOKEN!,
    },
    body: JSON.stringify({
      email: "admin@github-review.test",
      fullName: "GitHub Review Admin",
    }),
  });
  const adminCookie = cookieFrom(response);
  const adminLogin = await readJson<{ csrfToken: string }>(response);
  assert.equal(response.status, 200);

  response = await fetch(`${baseUrl}/api/integrations`, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      Cookie: adminCookie,
      "X-CSRF-Token": adminLogin.csrfToken,
    },
    body: JSON.stringify({
      provider: "github_app",
      displayName: "GitHub App Review Test",
      secrets: {
        app_id: "12345",
        installation_id: "67890",
        private_key: githubAppPrivateKeyPem,
      },
    }),
  });
  assert.equal(response.status, 201);

  response = await fetch(`${baseUrl}/api/settings`, {
    headers: { Cookie: adminCookie },
  });
  const settings = await readJson<{
    runtime: {
      reviewActionProvider: string;
      reviewActionsEnabled: boolean;
      localReviewActionsEnabled: boolean;
      githubAppReviewActionsEnabled: boolean;
      rollbackActionProvider: string;
      rollbackActionsEnabled: boolean;
      githubAppRollbackActionsEnabled: boolean;
      hermesMemoryEvictionConfigured: boolean;
    };
  }>(response);
  assert.equal(response.status, 200);
  assert.equal(settings.runtime.reviewActionProvider, "github-app-adapter");
  assert.equal(settings.runtime.reviewActionsEnabled, true);
  assert.equal(settings.runtime.localReviewActionsEnabled, false);
  assert.equal(settings.runtime.githubAppReviewActionsEnabled, true);
  assert.equal(settings.runtime.rollbackActionProvider, "github-app-hermes-adapter");
  assert.equal(settings.runtime.rollbackActionsEnabled, true);
  assert.equal(settings.runtime.githubAppRollbackActionsEnabled, true);
  assert.equal(settings.runtime.hermesMemoryEvictionConfigured, true);

  response = await fetch(`${baseUrl}/api/jobs/${reviewJobId}/approve`, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      Cookie: adminCookie,
      "X-CSRF-Token": adminLogin.csrfToken,
    },
    body: JSON.stringify({
      mergeMethod: "squash",
      commitTitle: "feat: autonomous sandbox proof",
      commitMessage: "Verified through job approval.",
    }),
  });
  const approved = await readJson<{
    job: {
      status: string;
      currentPhase: string;
      pullRequest: {
        number: number;
        merged: boolean;
        mergeSha: string;
        state: string;
      };
      pipelineState: {
        logs: string[];
      };
    };
  }>(response);
  assert.equal(response.status, 200);
  assert.equal(approved.job.status, "COMPLETED");
  assert.equal(approved.job.currentPhase, "COMPLETED_MERGED");
  assert.equal(approved.job.pullRequest.number, 42);
  assert.equal(approved.job.pullRequest.merged, true);
  assert.equal(approved.job.pullRequest.mergeSha, "merge123abc");
  assert.equal(approved.job.pullRequest.state, "closed");
  assert.ok(approved.job.pipelineState.logs.some((line) => line.includes("merged via GitHub App")));

  assert.equal(githubApiRequests.at(-2)?.url, "/app/installations/67890/access_tokens");
  const mergeRequest = githubApiRequests.at(-1);
  assert.equal(mergeRequest?.method, "PUT");
  assert.equal(mergeRequest?.url, "/repos/acme/widgets/pulls/42/merge");
  assert.ok(mergeRequest?.authorization?.startsWith("Bearer ghs_do_not_return"));
  assert.deepEqual(JSON.parse(mergeRequest?.body || "{}"), {
    merge_method: "squash",
    commit_title: "feat: autonomous sandbox proof",
    commit_message: "Verified through job approval.",
    sha: "1234567890abcdef1234567890abcdef12345678",
  });

  response = await fetch(`${baseUrl}/api/jobs/${rollbackJobId}/rollback`, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      Cookie: adminCookie,
      "X-CSRF-Token": adminLogin.csrfToken,
    },
    body: JSON.stringify({}),
  });
  const rolledBack = await readJson<{
    job: {
      status: string;
      currentPhase: string;
      rollbackSnapshot: {
        branchDeleted: boolean;
        jobStateReverted: boolean;
        memoryEvicted: boolean;
      };
      pipelineState: {
        logs: string[];
      };
    };
  }>(response);
  assert.equal(response.status, 200);
  assert.equal(rolledBack.job.status, "ROLLED_BACK");
  assert.equal(rolledBack.job.currentPhase, "ROLLED_BACK");
  assert.equal(rolledBack.job.rollbackSnapshot.branchDeleted, true);
  assert.equal(rolledBack.job.rollbackSnapshot.jobStateReverted, true);
  assert.equal(rolledBack.job.rollbackSnapshot.memoryEvicted, true);
  assert.ok(rolledBack.job.pipelineState.logs.some((line) => line.includes("deleted via GitHub App")));
  assert.ok(rolledBack.job.pipelineState.logs.some((line) => line.includes("Agent memory context evicted")));
  assert.ok(rolledBack.job.pipelineState.logs.some((line) => line.includes("Eviction ID: evict_rollback_123")));

  assert.ok(githubApiRequests.some((request) => request.method === "GET" && request.url === "/repos/acme/widgets"));
  assert.ok(githubApiRequests.some((request) => request.method === "GET" && request.url === "/repos/acme/widgets/git/ref/heads/main"));
  assert.ok(githubApiRequests.some((request) => request.method === "GET" && request.url === "/repos/acme/widgets/git/ref/heads/feature%2Fhermes-rollback-branch"));
  const deleteRequest = githubApiRequests.find((request) => request.method === "DELETE" && request.url === "/repos/acme/widgets/git/refs/heads/feature%2Fhermes-rollback-branch");
  assert.ok(deleteRequest, "expected branch deletion request");
  assert.ok(deleteRequest.authorization?.startsWith("Bearer ghs_do_not_return"));

  assert.equal(hermesEvictionRequests.length, 1);
  const hermesRequest = hermesEvictionRequests[0];
  assert.equal(hermesRequest.authorization, "Bearer hermes-evict-token");
  const hermesPayload = JSON.parse(hermesRequest.body || "{}");
  assert.deepEqual(hermesPayload, {
    jobId: rollbackJobId,
    executionId: `exec_${rollbackJobId}`,
    hermesSessionId: `hermes_${rollbackJobId}`,
    gitBranch: "feature/hermes-rollback-branch",
    targetRepository: "acme/widgets",
    requestedByEmail: "admin@github-review.test",
    requestedAt: hermesPayload.requestedAt,
  });
  assert.ok(hermesPayload.requestedAt);
});
